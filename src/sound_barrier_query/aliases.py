def expand_query_with_aliases(query: str) -> str:
    terms = _expanded_terms(query)
    if not terms:
        return query
    return " ".join([query, *terms])


def scope_terms_for_query(query: str) -> list[str]:
    return _matching_terms(query, SCOPE_ALIASES)


def item_terms_for_query(query: str) -> list[str]:
    return _matching_terms(query, ITEM_ALIASES)


def item_group_terms_for_query(query: str) -> list[str]:
    return _matching_terms(query, ITEM_GROUP_ALIASES)


def material_item_terms_for_query(material: str, query: str) -> list[str]:
    material_groups = MATERIAL_ITEM_GROUPS.get(material, {})
    if not material_groups:
        return []
    terms: list[str] = []
    for group in item_group_terms_for_query(query):
        terms.extend(material_groups.get(group, []))
    return _dedupe(terms)


def _expanded_terms(query: str) -> list[str]:
    terms: list[str] = []
    for alias_map in (MATERIAL_ALIASES, ITEM_ALIASES, ITEM_GROUP_ALIASES, SCOPE_ALIASES):
        terms.extend(_matching_terms(query, alias_map))
    return _dedupe(terms)


def _matching_terms(query: str, alias_map: dict[str, list[str]]) -> list[str]:
    normalized_query = normalize(query)
    if not normalized_query:
        return []

    matches: list[str] = []
    for canonical, aliases in alias_map.items():
        normalized_aliases = [normalize(alias) for alias in aliases]
        if any(alias and alias in normalized_query for alias in normalized_aliases):
            matches.append(canonical)
            matches.extend(aliases)
    return _dedupe(matches)


def _dedupe(values: list[str]) -> list[str]:
    seen: set[str] = set()
    result: list[str] = []
    for value in values:
        key = normalize(value)
        if not key or key in seen:
            continue
        seen.add(key)
        result.append(value)
    return result
